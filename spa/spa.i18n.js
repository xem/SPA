/** SPA I18n **/

SPA.i18n = {
};


/*
* _ or SPA.i18n._
* @param key: source string
* @param context: string context (optional)
* Translate a key in a given locale
*/

var _ = SPA.i18n._ = function(key, context){
 return key;
}

var _n = SPA.i18n._n = function(key_singular, key_plural, context, number){
 return key_singular;
}
